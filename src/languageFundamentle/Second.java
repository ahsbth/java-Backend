package languageFundamentle;

public class Second {

	public static void main(String[] args) {
		//Data types understanding
		
//		Byte Data Type 
//		Range -128 to 127 and memory is 1 byte(8 bits)
		byte b=-128;
		System.out.println(b);
		
//		short data type
//		Range -32768 to +32767 memory 16 bits(2byte)
		short s=1223;
		System.out.println(s);

//		integer data type
//		Range -2,147,483,648 to 2,147,483,647 and memory 4 byte
		int i=2434525;
		System.out.println(i);
		
//		long data type 
//		Range -2^63 to +2^63 memory 8 byte(64bits)
		long l=99l;
		System.out.println(l);
		
//		float data type
//		Range 32-bit IEEE 754 memory is 32 bits(4byte)
		float f=3.4f;
		System.out.println(f);
		
//		double data type
//		Range 4.9e-324 to 1.8e+308 memory 64bits(8byte)
		double d=8090.89d;
		System.out.println(d);
		
//		boolean data type
//		store either true or false by default false memory size: NA
		boolean bn;
		System.out.println(b);
		
//		Character data type
//		Range 0 to 65565 memory 2byte(16bits)
		char c='a';
		System.out.println(c);
		
		
		
		
	}

}
